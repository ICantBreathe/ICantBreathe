const int freedom_x = 10, freedom_y = 5, freedom_r = 3.5;

int main() {
    int cnt = sizeof(i) + sizeof(c) + sizeof(m) + sizeof(r);
    struct winsize size;
    ioctl(STDOUT_FILENO, TIOCGWINSZ, &size);
    for (int ii = 0; ii < size.ws_row - 1; ii++) {
        for (int jj = 0; jj < size.ws_col; jj++) {
            putchar(all[(ii*size.ws_col + jj)%cnt]);
        }
    }

    return 0;
}
